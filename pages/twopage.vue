<h5>・<span style="border-bottom: solid 3px #4169e1;">雪氷害の種類</span> :</h5>
<h6>&ensp;#積雪害</h6>
<p>&ensp;&ensp;長時間の降雪で、道路や歩道、経路などが埋もれる</p>
<h6>&ensp;#雪圧害</h6>
<p>&ensp;&ensp;雪の重みで、家屋や樹木が損壊する</p>
<h6>&ensp;#雪崩害</h6>
<p>&ensp;&ensp;山の斜面に積もった雪が崩壊し、人や建物を巻き込みながら滑り落ちていく</p>
<h6>&ensp;#着雪害</h6>
<p>&ensp;&ensp;電線などに雪が積もり、電柱の傾斜や電線が切断される</p>
<h6>&ensp;#風雪害</h6>
<p>&ensp;&ensp;強風とともに雪が降ることで、視界が悪化する</p>
<h6>&ensp;#その他</h6>
<p>&ensp;&ensp;雪解けなどによって路面が凍結し、転倒や転落、スリップ事故が起こる</p>
<h6>※ <font color=#dc143c>いずれも死亡事故が起こる危険な災害のため、雪への対策は欠かせません。</font><br>
&ensp;&ensp;以下に対策を載せていますので参考にしてください。</h6>
<h5>・<span style="border-bottom: solid 3px #4169e1;">被害を抑えるためにできる対策</span> :</h5>
<h5>&ensp;1.定期的に除雪作業を行う</h5>
<p>&ensp;&ensp;家屋の倒壊や落雪を避けるためにも定期的な除雪は重要です。<br>
&ensp;&ensp;しかし、雪害で一番多い被害は除雪作業中の事故のため、除雪作業を行う際は天候や作業環境をよく確認する必要があります。</p>
<h6>&ensp;※以下は首相官邸が呼びかけている<font color=#dc143c><strong>「命を守る除雪中の事故防止10箇条」</strong></font>です。</h6>
<div class="img">
        <img src="~/assets/yuki.png" alt="yuki"></div>
<h5>&ensp;2.大雪の際にはなるべく外出をしない</h5>
<p>&ensp;&ensp;大雪予報の場合は、転倒防止のため不要不急の外出はできるだけ避けましょう。<br>
  &ensp;&ensp;毎年雪が降る地域であっても、天候や環境によっては転倒や落雪などの事故が起こる危険性があります。<br>
  &ensp;&ensp;特に大雪の次の日に歩くと、路面が凍結している可能性が高く危険です。</p>
<h5>&ensp;3.雪道を安全に歩く練習をしておく</h5>
<p>&ensp;&ensp;雪道は小さな歩幅で靴の裏全体をつけて歩くと転倒しにくくなります。<br>
  &ensp;&ensp;また、靴も靴底がゴムになっている長靴などがおすすめです。<br>
  &ensp;&ensp;万が一転んでしまった場合でも、怪我をしないよう帽子や手袋といった冬の装備も身に着けておくと安心です。</p>
<h5>&ensp;4.車の運転をする場合には、路面やタイヤを確認する</h5>
<p>&ensp;&ensp;大雪が降った日はできるだけ車の運転を避けるほうが無難です。<br>
  &ensp;&ensp;どうしても運転しなければならない場合は、必ずスレッドタイヤに履き替えておきましょう。<br>
  &ensp;&ensp;普通のタイヤのまま運転すると、路面の凍結によるスリップ事故の危険性が高くなります。<br>
  &ensp;&ensp;運転をする際は、路面やタイヤの状況を確認し、急ブレーキやスピードを出さない慎重な運転が必要です。</p>
