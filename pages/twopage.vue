<p>お住まいの地域に⼟砂災害警戒情報（警戒レベル４相当情報）が発表されたら、自治体からの警戒レベル４避難指⽰の発令に留意し、<br>
⼟砂キキクル（⼤⾬警報（⼟砂災害）の危険度分布）などを参考にし、地域内の方々に声をかけあい近くの避難場所など、安全な場所に避難しましょう。<br>
特に、お年寄りや障害のある人など避難に時間がかかる人や、夜中に大雨が想定される場合は、警戒レベル３高齢者等避難が発令されたタイミング等で早めに避難することがより安全です。<br>
⼟砂災害の多くは⽊造の１階で被災しています。浸水などで避難場所への避難が困難なときは、近くの頑丈な建物の２階以上や、家の中でより安全な場所（がけから離れた部屋や２階など）に移動しましょう。</p>
